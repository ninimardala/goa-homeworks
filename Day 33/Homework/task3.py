"""საკლასო დავალება 4: სიის მანიპულირება და აგრეგაცია
შექმენით ტემპერატურის სია, რომელიც შეიცავს შემდეგ მნიშვნელობებს, რომლებიც წარმოადგენს დღიურ ტემპერატურას: [72, 68, 75, 70, 78, 74, 71].
გამოთვალეთ და დაბეჭდეთ უმაღლესი ტემპერატურა max() ფუნქციის გამოყენებით.
გამოთვალეთ და დაბეჭდეთ ყველაზე დაბალი ტემპერატურა min() ფუნქციის გამოყენებით.
გამოთვალეთ და დაბეჭდეთ საშუალო ტემპერატურა sum() ფუნქციის გამოყენებით სიის სიგრძეზე გაყოფილი.
გამოიყენეთ სიის გააზრება ახალი სიის შესაქმნელად, სახელწოდებით above_70, რომელიც შეიცავს მხოლოდ 70 გრადუსზე ზემოთ ტემპერატურას.
ამობეჭდეთ ზემოთ_70 სია."""





temperatures = [72, 68, 75, 70, 78, 74, 71]

highest_temp = max(temperatures)
print("უმაღლესი ტემპერატურა:", highest_temp)

lowest_temp = min(temperatures)
print("დაბალი ტემპერატურა:", lowest_temp)

average_temp = sum(temperatures) / len(temperatures)
print("საშუალო ტემპერატურა:", average_temp)

above_70 = [temp for temp in temperatures if temp > 70]
print("70 გრადუსზე ზემოთ ტემპერატურები:", above_70)