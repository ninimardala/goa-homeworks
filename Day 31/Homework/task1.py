"""კალკულატორი
განსაზღვრეთ ფუნქცია სახელწოდებით simple_calculator, რომელიც იღებს სამ არგუმენტს:
num1: პირველი რიცხვი (მთლიანი ან float).
num2: მეორე რიცხვი (მთლიანი ან float).
ოპერაცია: სტრიქონი,
რომელიც განსაზღვრავს შესასრულებლად ოპერაციას.
ის შეიძლება იყოს „დამატება“, „გამოკლება“, „გამრავლება“ ან „გაყოფა“.
ფუნქციის შიგნით,
გამოიყენეთ if და elif განცხადებები,
რათა დაადგინოთ რომელი ოპერაცია უნდა შეასრულოთ ოპერაციის არგუმენტის მნიშვნელობიდან გამომდინარე.
ფუნქციამ უნდა დააბრუნოს ოპერაციის შედეგი.
თუ ოპერაცია არის „გაყოფა“ და num2 არის 0, ფუნქციამ უნდა დააბრუნოს „შეცდომა: ნულზე გაყოფა შეუძლებელია“.
ჩაწერეთ კოდი,
რომ გამოიძახოთ ფუნქცია სხვადასხვა ოპერაციებით და დაბეჭდოთ შედეგები."""

def simple_calculator(num1, num2, operation):
    if operation == "დამატება":
        return num1 + num2
    elif operation == "გამოკლება":
        return num1 - num2
    elif operation == "გამრავლება":
        return num1 * num2
    elif operation == "გაყოფა":
        if num2 == 0:
            return "შეცდომა: ნულზე გაყოფა შეუძლებელია"
        else:
            return num1 / num2
    else:
        return "შეცდომა: არასწორი ოპერაცია"
try:
    num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
    num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))
    operation = input("შეიყვანეთ ოპერაცია (დამატება, გამოკლება, გამრავლება, გაყოფა): ").strip()
    result = simple_calculator(num1, num2, operation)
    print(f"შედეგი: {result}")
except ValueError:
    print("შეიყვანეთ ვალიდური რიცხვები.")
